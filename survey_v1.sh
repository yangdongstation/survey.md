问卷设计
【堕落程度】10题，总权重20分

【社交程度】7题，总权重14分

【心理健康】5题，总权重10分

【自毁倾向】5题，总权重10分

【性癖】23题，总权重46分

整体总分为100分，各题均按回答情况获得相应分值（建议每题均匀分值，如堕落部分每题2分，社交部分每题2分，心理健康和自毁倾向每题2分，性癖部分每题约2分）。

【一、堕落程度】（10题，总计20分，每题2分）
喝醉过吗？

逃课过吗？

染上烟瘾了吗？

因违纪被谈过吗？

因严重违纪被处分过吗？

用过非正当药物吗？

吸过毒吗？

因违法被警察问过吗？

霸凌过别人或被霸凌过吗？

夜不归宿、在公共场合裸奔过吗？

【二、社交程度】（7题，总计14分，每题2分）
平时经常牵手或拥抱别人吗？

主动约会过吗？

有维持稳定恋爱关系过吗？

在外面公开接吻过吗？

社交平台上晒过性感或私密照片吗？

收到过别人发来的暧昧图片或视频吗？

积极参加聚会、社交活动吗？

【三、心理健康】（5题，总计10分，每题2分）
曾长时间情绪低落过吗？

经常感到焦虑紧张吗？

压力大时觉得自己无力应对吗？

遇到情绪问题曾找过心理咨询吗？

对自己的性别或身份感到困惑吗？

【四、自毁倾向】（5题，总计10分，每题2分）
自残过吗（比如割腕、撞击等）？

有过自杀念头或尝试过自杀吗？

常常借酒消愁吗？

用药物来缓解情绪低落过吗？

冲动时做出过危险行为吗？

【五、性癖】（23题，总计46分，每题约2分）
自慰时尝试过多种方法吗？

买过情趣用品并用过吗？

拍过自己自慰或性行为的视频/照片吗？

分享过自己的私密影像吗？

跟不同性别或身份的人发生过关系吗？

在公共或半公共场合做过爱吗？

尝试过多种性爱体位吗？

玩过角色扮演或情趣调教吗？

穿过性感服装展示过自己吗？

感觉性欲有时会影响生活吗？

为了性欲主动找过多个伴侣吗？

参与过群交或多人性爱吗？

用过食物或日常用品增加性刺激吗？

性交过程中录过视频吗？

用过情趣玩具辅助性行为吗？

因性行为尝试遭过争议或反对吗？

曾因性幻想去追求非传统体验吗？

在非私密场合表现过挑逗行为吗？

经常思考自己的性取向或偏好吗？

觉得自己的性行为比常人更极端吗？

因性冲动做过冒险行为吗？

因性事闹过麻烦（社交或法律）吗？

感到自己对性有成瘾倾向吗？

【加权说明】
各部分原始得分计算：

堕落程度：10题×2分 = 20分

社交程度：7题×2分 = 14分

心理健康：5题×2分 = 10分

自毁倾向：5题×2分 = 10分

性癖：23题×约2分 = 46分

最终总分：
将各部分得分相加，共计100分。
研究者可根据需要调整个别题目的权重，但要求各部分最终得分按上述比例折算。

这份问卷在题目措辞上借鉴了原文件简洁直接的风格，同时调整了题目数量和各部分比例，便于后续数据统计和分析。
